async function upsertLesson(params: { courseMongoId: any; spec: LessonSpec }) {
  const { courseMongoId, spec } = params;
  const lessonId = `${COURSE_ID}_DAY_${spec.day}`;

  const existing = await Lesson.findOne({ courseId: courseMongoId, lessonId }).select({ _id: 1 }).lean();
  if (existing && ONLY_MISSING_LESSONS) {
    console.log(`‚è≠Ô∏è  ${lessonId} exists (skip; onlyMissingLessons=true)`);
    return;
  }

  const doc = {
    lessonId,
    courseId: courseMongoId,
    dayNumber: spec.day,
    displayOrder: spec.day,
    title: spec.title,
    content: buildContent(spec),
    language: 'ru',
    isActive: true,
    emailSubject: buildEmailSubject(spec),
    emailBody: buildEmailBody(spec),
  };

  if (!APPLY) {
    console.log(`üìù Would upsert ${lessonId} (${existing ? 'update' : 'create'})`);
    return;
  }

  await Lesson.updateOne({ courseId: courseMongoId, lessonId }, { $set: doc }, { upsert: true }).exec();
  console.log(`‚úÖ Upserted ${lessonId}`);
}

async function main() {
  await connectDB();

  const course = await Course.findOne({ courseId: COURSE_ID, isActive: true }).select({ _id: 1, courseId: 1, language: 1, durationDays: 1 }).lean();
  if (!course) throw new Error(`Active course not found: ${COURSE_ID}`);

  console.log(`‚úÖ Course ${COURSE_ID} loaded (${APPLY ? 'APPLY' : 'DRY-RUN'})`);
  console.log(`- onlyMissingLessons: ${ONLY_MISSING_LESSONS}`);

  for (const spec of lessons) {
    await upsertLesson({ courseMongoId: course._id, spec });
  }

  process.exit(0);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});

